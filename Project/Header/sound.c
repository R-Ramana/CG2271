uint16_t numNotes = 266;

void setPWMFreq(TPM_Type *timer, uint8_t channel, uint16_t freq) {
  timer->MOD = FREQ_2_MOD(freq);
  TPM_CnV_REG(timer,channel) = FREQ_2_MOD(freq)/2;
}


void playTone(TPM_Type *timer, uint8_t channel, uint16_t tone, uint8_t duration) {
  setPWMFreq(timer, channel, tone);
  delay2(duration*0xFFF);
}

void playNote(TPM_Type *timer, uint8_t channel, char note, uint8_t duration) {
  char names[] = { 'c', 'd', 'e', 'f', 'g', 'a', 'b', 'C' };
  uint16_t tones[] = { 1915, 1700, 1519, 1432, 1275, 1136, 1014, 956 };

  // play the tone corresponding to the note name
  for (int i = 0; i < 8; i++) {
    if (names[i] == note) {
      playTone(timer, channel, tones[i], duration);
    }
  }
}


void playSong(TPM_Type *timer, uint8_t channel) {
  for (int i = 0; i < numNotes; i++) {
    uint8_t noteDuration = TEMPO / noteDurations[i];
    playTone(timer, channel, melody[i], noteDuration);
    delay2(noteDuration * 1.45);
  }
}
